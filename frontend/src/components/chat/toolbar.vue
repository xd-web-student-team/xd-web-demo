<script>
import Vue from "vue";
export default {
  name: "toolbar",
  data() {
    var validateGroupName = (rule, value, callback) => {
      //检查群名是否重复
      this.getRequest("chat/checkGroupName?groupName=" + value).then((resp) => {
        if (resp != 0) {
          callback(new Error("该群名已被注册"));
        } else {
          callback();
        }
      });
    };
    var validateJoinGroup = (rule, value, callback) => {
      //检查群名是否存在
      this.getRequest("chat/checkGroupName?groupName=" + value).then((resp) => {
        if (resp == 0) {
          callback(new Error("该群不存在"));
        } else {
          this.getRequest("chat/checkJoined?idGroup=" + resp.id).then(
            (resp2) => {
              if (resp2 == 0) {
                callback();
              } else {
                callback(new Error("已加入该群"));
              }
            }
          );
        }
      });
    };
    return {
      user: JSON.parse(window.sessionStorage.getItem("user")),
      feedBackDialogVisible: false,
      feedBackContent: "",
      buildGroupDialog: false,
      joinGroupDialog: false,
      beFriendDialog: false,
      groupForm: {
        groupName: "",
      },
      groupRules: {
        groupName: [
          { required: true, message: "请输入群聊名称", trigger: "blur" },
          { validator: validateGroupName, trigger: "blur" },
        ],
      },
      joinGroupRules: {
        groupName: [
          { required: true, message: "请输入群聊名称", trigger: "blur" },
          { validator: validateJoinGroup, tirgger: "blur" },
        ],
      },
    };
  },
  methods: {
    //打开建群对话框
    showBuildGroupDialog() {
      this.buildGroupDialog = true;
    },
    //打开加群对话框
    showJoinGroupDialog() {
      this.joinGroupDialog = true;
    },
    //打开加好友对话框
    showBeFriendDialog() {
      this.beFriendDialog = true;
    },
    //建群
    buildGroup() {
      this.$refs.groupForm.validate((valid) => {
        if (valid) {
          this.postRequest("/chat/buildGroup", this.groupForm).then((resp) => {
            Vue.set(this.$store.state.sessions["群聊"], resp.id, {
              groupMsgContentList: [],
              idGroup: resp.id,
            });
            Vue.set(
              this.$store.state.groups,
              this.$store.state.groups.length,
              resp
            );
            this.$message({
              message: "创建成功",
              type: "success",
            });
          });
        } else {
          console.log(valid);
          this.$message.error("请正确填写信息！");
          return false;
        }
      });
    },
    //加群
    joinGroup() {
      this.$refs.groupForm.validate((valid) => {
        if (valid) {
          this.postRequest("/chat/joinGroup", this.groupForm).then((resp) => {
            Vue.set(
              this.$store.state.groups,
              this.$store.state.groups.length,
              resp
            );
            let idGroup = resp.id;
            this.getRequest(
              "/groupMsgContent/queryByIdGroup?idGroup=" + idGroup
            ).then((resp) => {
              if (!resp) {
                Vue.set(this.$store.state.sessions["群聊"], idGroup, {
                  groupMsgContentList: [],
                  idGroup: idGroup,
                });
              } else Vue.set(this.$store.state.sessions["群聊"], idGroup, resp);
            });
            this.$message({
              message: "加入成功",
              type: "success",
            });
          });
        }
      });
    },
    //加好友
    beFriend() {},
    closeBuildGroupDialog(done) {
      this.groupForm = {
        groupName: "",
      };
      done();
    },
    //退出系统
    exitSystem() {
      this.$confirm("你是否要退出系统吗?", "系统提示", {
        confirmButtonText: "确定",
        cancelButtonText: "取消",
        type: "warning",
      })
        .then(() => {
          this.getRequest("/logout");
          sessionStorage.removeItem("user");
          //清除SessionStorage中保存的state
          if (sessionStorage.getItem("state")) {
            sessionStorage.removeItem("state");
          }
          //关闭连接
          this.$store.dispatch("disconnect");
          this.$router.replace("/");
        })
        .catch(() => {
          this.$message({
            type: "info",
            message: "已取消操作",
          });
        });
    },
    //选择聊天列表
    chooseChatList(listName) {
      this.$store.commit("changeCurrentList", listName);
    },
    //打开意见反馈对话框
    showFeedbackDialog() {
      this.feedBackContent = "";
      this.feedBackDialogVisible = true;
    },
    //处理反馈消息邮件发送
    handleFeedbackSend() {
      let msgObj = {};
      msgObj.userId = this.user.id;
      msgObj.nickname = this.user.nickname;
      msgObj.username = this.user.username;
      msgObj.content = this.feedBackContent;
      console.log(msgObj);
      this.postRequest("/mail/feedback", msgObj).then((resp) => {
        if (resp) {
          this.feedBackDialogVisible = false;
        }
      });
    },
    //清空聊天记录
    clearChatHistory() {
      this.$confirm(
        "此操作将永久删除本地聊天记录(群聊记录会在下次登录时恢复), 是否继续?",
        "提示",
        {
          confirmButtonText: "确定",
          cancelButtonText: "取消",
          type: "warning",
        }
      )
        .then(() => {
          //清除本地的localStorage中的聊天记录
          if (localStorage.getItem("chat-session")) {
            localStorage.removeItem("chat-session");
          }
          //清除Vuex中保存的记录
          this.$store.state.sessionStorage = {};
          //清除SessionStorage中保存的state
          if (sessionStorage.getItem("state")) {
            sessionStorage.removeItem("state");
          }
          this.$message({
            type: "success",
            message: "删除成功",
          });
        })
        .catch(() => {
          this.$message({
            type: "info",
            message: "已取消删除",
          });
        });
    },
  },
};
</script>

