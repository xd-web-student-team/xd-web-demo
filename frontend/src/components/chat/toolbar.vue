        status-icon
        :rules="joinGroupRules"
        ref="groupForm"
      >
        <el-form-item prop="groupName">
          <el-input
            v-model="groupForm.groupName"
            autocomplete="off"
            placeholder="请输入群聊名称"
            @keyup.enter.native="joinGroup('groupForm')"
          ></el-input>
        </el-form-item>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button
          type="primary"
          @click="joinGroup('groupForm')"
          style="width: 100%"
          >加入群聊</el-button
        >
      </div>
    </el-dialog>
